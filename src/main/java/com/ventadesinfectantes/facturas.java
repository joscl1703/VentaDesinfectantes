    private void updateInvoiceIndicator() {
        List<InvoiceInDB> facturas = Repositories.getInstance().invoice.getAll();
        facturasIndicador.setText(String.valueOf(facturas.size()));
        if(facturas.size() >= 5) {
            double facturacionTotal = 0;
            int litrosArticuloUno = 0;
            int facturasMayores = 0;
            for (InvoiceInDB next : facturas) {
                double totalFactura = next.pricePerLiter * next.quantitySoldInLiters;
                facturacionTotal = facturacionTotal + totalFactura;
                if(next.articleCode.equals("1")) {
                    litrosArticuloUno += next.quantitySoldInLiters;
                };
                if(totalFactura > 50000) {
                    facturasMayores++;
                }
            }
            JOptionPane.showMessageDialog(this, 
                    "Facturación total: ".concat(String.valueOf(facturacionTotal))
                    +"\n"
                    + "Cantidad en litros vendidos del articulo 1: ".concat(String.valueOf(litrosArticuloUno))
                    + "\n"
                    + "Numero de facturas mayores a 50000bs: ".concat(String.valueOf(facturasMayores))
            );
            Repositories.getInstance().invoice.deleteAll();
            facturasIndicador.setText("0");
        }
    }

