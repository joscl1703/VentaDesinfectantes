    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String articleCode = this.articleCode.getText();
        double quantityInLiters = (double) this.quantityInLiters.getValue();
        int selector = this.pricePerLiter.getSelectedIndex();
        double pricePerLiters;
        pricePerLiters = switch (selector) {
            case 0 -> 6;
            case 1 -> 10;
            default -> 15;
        };
        Invoice newInvoice = new Invoice(articleCode, quantityInLiters, pricePerLiters);
        try {
            Repositories.getInstance().invoice.insert(newInvoice);
            JOptionPane.showMessageDialog(this, "Factura añadida con exito");
            updateInvoiceIndicator();
        } catch (RuntimeException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }//GEN-LAST:event_jButton2ActionPerformed

